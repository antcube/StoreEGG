class Product {
    constructor(id, name, description, price, discount, img, colors, linkTo) {
        // atributos
        this.id = id;
        this.name = name;
        this.description = description;
        this.price = price;
        this.discount = discount;
        this.img = img;
        this.colors = colors;
        this.linkTo = linkTo;
    }
}

export default Product;